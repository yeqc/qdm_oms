package com.work.shop.vo;

public class DescModule {

	private String content;
	
	private String intros;
	
	private Integer moduleId;
	
	private String moduleName;
	
	private String required;
	
	private String tplUrls;
	
	private String type;

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getIntros() {
		return intros;
	}

	public void setIntros(String intros) {
		this.intros = intros;
	}

	public Integer getModuleId() {
		return moduleId;
	}

	public void setModuleId(Integer moduleId) {
		this.moduleId = moduleId;
	}

	public String getModuleName() {
		return moduleName;
	}

	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}

	public String getRequired() {
		return required;
	}

	public void setRequired(String required) {
		this.required = required;
	}

	public String getTplUrls() {
		return tplUrls;
	}

	public void setTplUrls(String tplUrls) {
		this.tplUrls = tplUrls;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
}

