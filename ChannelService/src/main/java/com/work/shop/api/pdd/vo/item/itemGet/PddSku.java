package com.work.shop.api.pdd.vo.item.itemGet;

public class PddSku {

	private String spec;
	private Long sku_id;
	private Long sku_quantity;
	private String outer_id;
	public String getSpec() {
		return spec;
	}
	public void setSpec(String spec) {
		this.spec = spec;
	}
	public Long getSku_id() {
		return sku_id;
	}
	public void setSku_id(Long sku_id) {
		this.sku_id = sku_id;
	}
	public Long getSku_quantity() {
		return sku_quantity;
	}
	public void setSku_quantity(Long sku_quantity) {
		this.sku_quantity = sku_quantity;
	}
	public String getOuter_id() {
		return outer_id;
	}
	public void setOuter_id(String outer_id) {
		this.outer_id = outer_id;
	}
	
	
	
}
