package com.work.shop.api.mogu.vo.item.itemGet;

public class ItemSku {

	private String sku_id;
	private String sku_stock;
	private String sku_code;
	private String sku_price;
	private String sku_prop;
	public String getSku_id() {
		return sku_id;
	}
	public void setSku_id(String sku_id) {
		this.sku_id = sku_id;
	}
	public String getSku_stock() {
		return sku_stock;
	}
	public void setSku_stock(String sku_stock) {
		this.sku_stock = sku_stock;
	}
	public String getSku_code() {
		return sku_code;
	}
	public void setSku_code(String sku_code) {
		this.sku_code = sku_code;
	}
	public String getSku_price() {
		return sku_price;
	}
	public void setSku_price(String sku_price) {
		this.sku_price = sku_price;
	}
	public String getSku_prop() {
		return sku_prop;
	}
	public void setSku_prop(String sku_prop) {
		this.sku_prop = sku_prop;
	}
	
}
